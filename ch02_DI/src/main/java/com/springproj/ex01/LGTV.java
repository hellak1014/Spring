package com.springproj.ex01;

public class LGTV {

	public void turnOn() {
		//켜는 기능
		System.out.println("Lgtv 전원을 켜다.");
	}
	
	public void turnOff() {
		//끄는 기능
		System.out.println("Lgtv 전원을 끈다");
	}
	
	public void soundUp() {
		//소리 올리는 기능
		System.out.println("Lgtv 소리를 켜다");
	}
	
	public void soundOff() {
		//소리를 낮추는 기능
		System.out.println("Lgtv 소리를 끈다.");
	}
	
}
